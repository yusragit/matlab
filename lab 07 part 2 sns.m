t1=-10:0.01:-2;
t2=-2:0.01:-1;
t3=-1:0.01:0;
t4=0:0.01:1;
t5=1:0.01:10;
t=[t1 t2 t3 t4 t5];
x1=linspace(0,0,length(t1));
x2=linspace(0,1,length(t2));
x3=linspace(1,0,length(t3));
x4=linspace(0,1,length(t4));
x5=linspace(1,1,length(t5));
x=[x1 x2 x3 x4 x5];
xhm=fliplr(x);
xe=(x+xhm)/2;
xo=(x-xhm)/2;
subplot(411);plot(t,x);
xlabel(’t’);ylabel(’x’);
axis([-10 10 -2 4]);
grid on;
